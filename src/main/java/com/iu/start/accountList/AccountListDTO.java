package com.iu.start.accountList;

public class AccountListDTO {
	
	private long acId;
	private String acName;
	private double acRate;
	private int acSale;
	
	public long getAcId() {
		return acId;
	}
	public void setAcId(long acId) {
		this.acId = acId;
	}
	public String getAcName() {
		return acName;
	}
	public void setAcName(String acName) {
		this.acName = acName;
	}
	public double getAcRate() {
		return acRate;
	}
	public void setAcRate(double acRate) {
		this.acRate = acRate;
	}
	public int getAcSale() {
		return acSale;
	}
	public void setAcSale(int acSale) {
		this.acSale = acSale;
	}
	
	

}
